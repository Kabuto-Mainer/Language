#ifndef STRING_TABLE_H
#define STRING_TABLE_H

#include <stddef.h>

// ---------------------------------------------------------------------------------------------------
struct StringEntry_t
{
    char* string;
    size_t hash;

    StringEntry_t* next;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
struct StringTable_t
{
    StringEntry_t** buckets;
    size_t count;
    size_t size;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
const size_t StrT_START_SIZE = 10;
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
StringTable_t*  StrT_Create ();

StringEntry_t*  StrT_AddString (StringTable_t* table, const char* name);

void            StrT_Resize (StringTable_t* table);

void            StrT_Delete (StringTable_t* table);
// ---------------------------------------------------------------------------------------------------


#endif // STRING_TABLE_H
