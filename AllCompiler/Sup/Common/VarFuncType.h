#ifndef VAR_FUNC_TYPE_H
#define VAR_FUNC_TYPE_H


// ---------------------------------------------------------------------------------------------------
enum TypeLangVar
{
    LangStdVar,
    LangMassVar,
    LangString
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
struct LangVar_t
{
    u_int8_t ptr_lvl;
    u_int16_t amount;
    int offset;
    char* type;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
struct LangFunc_t
{
    u_int8_t ret_ptr_lvl;
    LangType_t* ret_type;
    int amount_arg;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
struct LangType_t
{
    char* name;
    int size;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
union ValueIdent_t
{
    LangVar_t* var;
    LangFunc_t* func;
    LangType_t* type;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
/// @brief Структура элемента таблицы имен с конечным автоматроном
struct Ident_t
{
    size_t hash;
    char* name;
    ValueIdent_t val;
    Ident_t* next;
};
// ---------------------------------------------------------------------------------------------------


#endif /* VAR_FUNC_TYPE_H */
