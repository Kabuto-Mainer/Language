// ---------------------------------------------------------------------------------------------------
enum TypeLangVar
{
    LangStdVar,
    LangMassVar,
    LangString
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
struct LangVar_t
{
    int point_level;
    char* type;
    int phase;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
struct LangFunc_t
{
    int ret_point_level;
    char* ret_type;
    int amount_arg;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
union ValueIndent_t
{
    LangVar_t* var;
    LangFunc_t* func;
};
// ---------------------------------------------------------------------------------------------------

// ---------------------------------------------------------------------------------------------------
/// @brief Структура элемента таблицы имен с конечным автоматроном
struct Indent_t
{
    size_t hash;
    char* name;
    ValueIndent_t val;
    Indent_t* next;
};
// ---------------------------------------------------------------------------------------------------


#endif /* VAR_FUNC_TYPE_H */
